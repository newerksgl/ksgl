package com.newer.ksgl.model.pojo;


public class Usercertificate {

  private long ucid;
  private long cid;
  private long csid;
  private String name;


  public long getUcid() {
    return ucid;
  }

  public void setUcid(long ucid) {
    this.ucid = ucid;
  }


  public long getCid() {
    return cid;
  }

  public void setCid(long cid) {
    this.cid = cid;
  }


  public long getCsid() {
    return csid;
  }

  public void setCsid(long csid) {
    this.csid = csid;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

}
