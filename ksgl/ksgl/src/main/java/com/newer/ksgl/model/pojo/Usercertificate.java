package com.newer.ksgl.model.pojo;


public class Usercertificate {

  private Long ucid;
  private Long cid;
  private Long csid;
  private String name;


  public Long getUcid() {
    return ucid;
  }

  public void setUcid(Long ucid) {
    this.ucid = ucid;
  }


  public Long getCid() {
    return cid;
  }

  public void setCid(Long cid) {
    this.cid = cid;
  }


  public Long getCsid() {
    return csid;
  }

  public void setCsid(Long csid) {
    this.csid = csid;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

}
